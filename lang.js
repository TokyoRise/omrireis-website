  /* ---- helpers ---- */
  function applyLang(lang) {
    var t = translations[lang];
    if (!t) return;

    // plain text
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      if (t[key] !== undefined) el.textContent = t[key];
    });

    // innerHTML (for elements containing links etc.)
    document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-html');
      if (t[key] !== undefined) el.innerHTML = t[key];
    });

    // placeholder
    document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-placeholder');
      if (t[key] !== undefined) el.placeholder = t[key];
    });

    // document direction & lang attribute
    document.documentElement.lang = lang;
    document.documentElement.dir = (lang === 'he') ? 'rtl' : 'ltr';

    // update toggle button label
    var btn = document.getElementById('lang-toggle-btn');
    if (btn) btn.textContent = (lang === 'he') ? 'EN' : '\u05e2\u05d1';
  }

  function injectToggle() {
    var navSocial = document.querySelector('.nav-social');
    if (!navSocial) return;

    var btn = document.createElement('button');
    btn.id = 'lang-toggle-btn';
    btn.className = 'lang-toggle';
    btn.setAttribute('aria-label', 'Toggle language');

    var current = localStorage.getItem('preferred-lang') || 'en';
    btn.textContent = (current === 'he') ? 'EN' : '\u05e2\u05d1';

    btn.addEventListener('click', function () {
      var next = (localStorage.getItem('preferred-lang') || 'en') === 'en' ? 'he' : 'en';
      localStorage.setItem('preferred-lang', next);
      applyLang(next);
    });

    // insert after nav-social, before hamburger button
    navSocial.parentNode.insertBefore(btn, navSocial.nextSibling);
  }

  /* ---- init ---- */
  document.addEventListener('DOMContentLoaded', function () {
    injectToggle();
    var saved = localStorage.getItem('preferred-lang') || 'en';
    applyLang(saved);
  });

